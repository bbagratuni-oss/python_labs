n = int(input('Введите размер массива: '))
while n <= 0:
    n = int(input('Размер должен быть больше нуля: '))

a = [0] * n
for i in range(n):
    a[i] = int(input(f'Введите {i}-й элемент: '))

# Поиск наиболее длинной непрерывной последовательности возрастающих простых чисел
best_start = -1   # начало лучшего участка
best_len = 0      # длина лучшего участка

cur_start = -1    # начало текущего участка
cur_len = 0       # длина текущего участка

for i in range(n):
    # проверка простоты a[i] без функций
    x = a[i]
    is_prime = x >= 2
    d = 2
    while is_prime and d * d <= x:
        if x % d == 0:
            is_prime = False
        d += 1

    if not is_prime:
        # текущий участок обрывается
        if cur_len > best_len:
            best_len = cur_len
            best_start = cur_start
        cur_len = 0
        cur_start = -1
    else:
        if cur_len == 0:
            # начинаем новый участок с этого простого числа
            cur_start = i
            cur_len = 1
        else:
            # продолжаем только если строго возрастаем относительно предыдущего элемента
            if a[i] > a[i - 1]:
                cur_len += 1
            else:
                # предыдущий участок закрываем и начинаем новый с текущего простого числа
                if cur_len > best_len:
                    best_len = cur_len
                    best_start = cur_start
                cur_start = i
                cur_len = 1

# сравниваем последний текущий участок с лучшим
if cur_len > best_len:
    best_len = cur_len
    best_start = cur_start

# Вывод результата
if best_len == 0:
    print('\nПодходящих последовательностей нет.')
else:
    print('\nНаиболее длинная возрастающая последовательность простых чисел:')
    # печатаем сам участок
    for i in range(best_start, best_start + best_len):
        print(a[i], end=' ')
    print(f'\nДлина: {best_len}')
    print(f'Индексы: от {best_start} до {best_start + best_len - 1}')







