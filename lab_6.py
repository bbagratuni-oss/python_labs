n = int(input('Введите размер массива: '))
while n <= 0:
    n = int(input('Размер должен быть больше нуля: '))

a = [0] * n
for i in range(n):
    a[i] = int(input(f'Введите {i}-й элемент: '))

# Поиск минимального и максимального отрицательных элементов
min_neg = None      # минимальное отрицательное
max_neg = None      # максимальное отрицательное
min_index = -1
max_index = -1

for i in range(n):
    if a[i] < 0:  # рассматриваем только отрицательные элементы
        if min_neg is None or a[i] < min_neg:
            min_neg = a[i]
            min_index = i
        if max_neg is None or a[i] > max_neg:
            max_neg = a[i]
            max_index = i

# Проверка, были ли отрицательные элементы
if min_index == -1 or max_index == -1:
    print('\nОтрицательные элементы отсутствуют.')
else:
    # Меняем местами найденные элементы
    a[min_index], a[max_index] = a[max_index], a[min_index]

    print('\nМассив после обмена минимального и максимального отрицательных элементов:')
    for i, el in enumerate(a):
        print(f'{i:2}-й элемент: {el}')

    print(f'\nМинимальный отрицательный ({min_neg}) был на позиции {min_index}')
    print(f'Максимальный отрицательный ({max_neg}) был на позиции {max_index}')