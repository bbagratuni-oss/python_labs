# Назначение: программа удаляет все положительные элементы списка.

n = int(input('Введите размер массива: '))
while n <= 0:
    n = int(input('Размер должен быть больше нуля: '))

a = [0] * n
for i in range(n):
    a[i] = int(input(f'Введите {i}-й элемент: '))

k = 0
for i in range(len(a)):
    if a[i] <= 0:
        a[k] = a[i]
        k += 1

a = a[:k]

print('\nПолученный массив:')
for i, el in enumerate(a):
    print(f'{i:2}-й элемент: {el}')




# Нарек Аветисян, группа ИУ7-12Б
# Назначение: программа добавляет после каждого положительного элемеента
# его удвоенное значение

n = int(input('Размер должен быть больше нуля: '))
while n <= 0:
    n = int(input('Размер должен быть больше нуля: '))

a = [0] * n
for i in range(n):
    a[i] = int(input(f'Введите {i}-й элемент: '))

# считаем положительные
pos = 0
for x in a:
    if x > 0:
        pos += 1

# расширяем список
for _ in range(pos):
    a.append(0)

# два указателя: i — конец старой части, j — конец новой
i = len(a) - 1 - pos
j = len(a) - 1

# раскладываем с конца: после положительного ставим его удвоение
while i >= 0:
    x = a[i]
    if x > 0:
        a[j] = 2 * x   # сначала записываем удвоенное вправо
        j -= 1
        a[j] = x       # затем сам элемент левее
        j -= 1
    else:
        a[j] = x
        j -= 1
    i -= 1

print('\nПолученный массив:')
for i, el in enumerate(a):
    print(f'{i:2}-й элемент: {el}')







