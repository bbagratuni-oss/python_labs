# Назначение программы: программа находит самый короткий элемент в списке строк,
# который не содержит пробелов.

# Ввод размера массива
n = int(input('Введите размер массива: '))
while n <= 0:
    n = int(input('Размер должен быть больше нуля: '))

# Создание списка и ввод элементов
a = [0] * n
for i in range(n):
    a[i] = input(f'Введите {i}-й элемент: ')

# Поиск минимальной длины строки без пробелов
min_len = -1
for x in a:
    if ' ' not in x and len(x) > 0:  # проверяем, что в строке нет пробелов
        if min_len == -1 or len(x) < min_len:
            min_len = len(x)  # обновляем минимальную длину

# Вывод результата
if min_len == -1:
    print("Нет элементов без пробелов")
else:
    print("Самые короткие элементы без пробелов:")
    for x in a:
        if ' ' not in x and len(x) == min_len:
            print(x)






