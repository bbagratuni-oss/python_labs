# Назначение: программа заменяет все цифры на пробелы

# Ввод количества строк
n = int(input("Введите размер списка: "))
a = [0] * n

# Ввод элементов списка
for i in range(n):
    a[i] = input(f"Введите {i}-й элемент: ")

# Замена всех цифр на пробелы в каждой строке
for i in range(len(a)):
    new_str = ''
    for ch in a[i]:
        if ch in '0123456789':          # если символ - цифра
            new_str += ' '        # заменяем на пробел
        else:
            new_str += ch
    a[i] = new_str                # записываем изменённую строку обратно в список

# Вывод результата
print("\nИзменённый список:")
for i, el in enumerate(a):
    print(f"{i}-й элемент: {el}")
